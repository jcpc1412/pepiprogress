import type { Enums } from '@/types/database';

export type CompoundType = Enums<'compound_type'>;

/**
 * On-device copy of the compound catalog (mirrors supabase/seed.sql).
 *
 * Why bundled: onboarding + the field-surfacing engine run local-first and
 * pre-account (spec 02/10), but the `compound` table's RLS is authenticated-only,
 * so an anonymous user can't fetch it. Bundling also keeps onboarding fully
 * offline. When auth + sync land, the hosted catalog becomes the source of truth
 * and this seed is reconciled by `slug` → catalog row.
 *
 * `slug` is the stable local id; it maps to the DB uuid on sync.
 */
export type CatalogCompound = {
  slug: string;
  canonicalName: string;
  aliases: string[];
  type: CompoundType;
  /** controlled=true => track-only, no AI dosing (spec 05/11). */
  controlled: boolean;
  effectTags: string[];
  monitoringTags: string[];
  commonUses: string[];
  /** Administered by injection (drives route defaults + the recon gate, P-03). */
  injectable: boolean;
  /** Ships as lyophilized powder needing BAC water. Pre-mixed injectables
   *  (e.g. testosterone-in-oil) are injectable but NOT reconstituted. The P-03
   *  BAC suggestion fires only when injectable && reconstituted. */
  reconstituted: boolean;
  /** Common vial strengths (mg) — defaults the reconstitution math; user-editable.
   *  Product packaging, not a dose recommendation. */
  commonVialSizesMg?: number[];
  /** True for user-created compounds (O-04); absent for bundled catalog rows. */
  custom?: boolean;
};

